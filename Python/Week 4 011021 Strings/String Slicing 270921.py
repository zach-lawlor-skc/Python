#String Slicing
#Extracting sub--string from string is called slicing

subject = ("Manchester United")
print (subject[11:17])
#This goes from index 11 to 17 which is the same as the last string.
print (subject[11:])
#Leaving the end of a range blank will tell Python the print the whole string up to the last index
#Can you show how to use this technique to print from the first index?

print (subject[:])
